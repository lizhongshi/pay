package com.dly.pay.entity;

public class OpenQuickpay {
	private String userName;
	private String phone;
	private String idCard;
	private String cardNo;
	private String shbh;
	private Double dbsxf;
	private Boolean isjf;
	private Double rate;
	private String address;
		
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getIdCard() {
		return idCard;
	}
	public void setIdCard(String idCard) {
		this.idCard = idCard;
	}
	public String getCardNo() {
		return cardNo;
	}
	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}
	public String getShbh() {
		return shbh;
	}
	public void setShbh(String shbh) {
		this.shbh = shbh;
	}
	public Double getDbsxf() {
		return dbsxf;
	}
	public void setDbsxf(Double dbsxf) {
		this.dbsxf = dbsxf;
	}
	public Boolean getIsjf() {
		return isjf;
	}
	public void setIsjf(Boolean isjf) {
		this.isjf = isjf;
	}
	public Double getRate() {
		return rate;
	}
	public void setRate(Double rate) {
		this.rate = rate;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
}
